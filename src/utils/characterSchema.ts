// Версионирование схемы персонажа: гарантия, что персонажи, созданные в этой
// версии приложения, останутся загружаемыми во всех будущих версиях.
//
// Как это работает:
//  - Каждый персонаж несёт `schemaVersion` (штампуется при загрузке/сохранении).
//  - Персонажи без поля (созданные до версионирования) считаются версией 0.
//  - При загрузке персонаж последовательно проходит миграции от своей версии
//    до текущей; каждая миграция — маленькая функция «N−1 → N».
//  - Персонаж из БОЛЕЕ НОВОЙ версии приложения (schemaVersion > текущей)
//    не мигрируется и не портится: неизвестные поля сохраняются как есть
//    (весь код обновляет персонажа спредом `{ ...character }`), штамп версии
//    никогда не понижается. UI показывает предупреждение (isFromNewerAppVersion).
//
// ПРАВИЛА для будущих изменений формата:
//  1. Добавление нового ОПЦИОНАЛЬНОГО поля версию НЕ повышает — старые данные
//     валидны, undefined обрабатывают дефолты (см. migrateCharacter в storage.ts).
//  2. Ломающее изменение (переименование/удаление/смена смысла поля):
//     поднять CHARACTER_SCHEMA_VERSION на +1 и добавить в MIGRATIONS запись
//     `N: (c) => { ... }`, переводящую персонажа с версии N−1 на N (мутирует
//     объект на месте). Миграция обязана быть тотальной: обработать и данные,
//     где поля нет вовсе.
//  3. НИКОГДА не удалять существующие миграции и не менять их номера — цепочка
//     должна проводить персонажа любой старой версии до текущей.
//  4. Не переиспользовать освободившиеся имена полей под другой смысл — старые
//     версии приложения читают неизвестные поля как есть.
import type { Character } from '../types';

/** Текущая версия схемы персонажа. */
export const CHARACTER_SCHEMA_VERSION = 1;

/** Цепочка миграций: MIGRATIONS[N] переводит персонажа с версии N−1 на N. */
const MIGRATIONS: Record<number, (c: Record<string, unknown>) => void> = {
  // 1: базовый штамп версии. Ломающих изменений формата ещё не было; все
  //    накопленные к этому моменту нормализации (equipment/currency-дефолты,
  //    amulet→accessory, feat.nameEn) идемпотентны и живут вне цепочки.
  1: () => {},
};

/**
 * Провести персонажа по цепочке миграций до текущей версии схемы (мутирует).
 * Данные из более новой версии приложения не трогает и штамп не понижает.
 */
export function applySchemaMigrations(character: Character): Character {
  const from = character.schemaVersion ?? 0;
  if (from >= CHARACTER_SCHEMA_VERSION) return character;
  for (let v = from + 1; v <= CHARACTER_SCHEMA_VERSION; v++) {
    MIGRATIONS[v]?.(character as unknown as Record<string, unknown>);
  }
  character.schemaVersion = CHARACTER_SCHEMA_VERSION;
  return character;
}

/**
 * Персонаж записан более новой версией приложения, чем эта.
 * Открывать и играть можно (неизвестные поля сохраняются), но новые
 * возможности могут не отображаться — UI показывает предупреждение.
 */
export function isFromNewerAppVersion(character: Character): boolean {
  return (character.schemaVersion ?? 0) > CHARACTER_SCHEMA_VERSION;
}
